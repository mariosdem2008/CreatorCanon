function readEnv(filePath) {
  if (!fs.existsSync(filePath)) return [];
  return fs.readFileSync(filePath, 'utf8').split(/\r?\n/);
}

function upsertEnv(filePath) {
  const existing = readEnv(filePath);
  const used = new Set();
  const next = existing.map((line) => {
    const match = line.match(/^([A-Za-z_][A-Za-z0-9_]*)=/);
    if (!match) return line;
    const key = match[1];
    if (!(key in localVars)) return line;
    used.add(key);
    return `${key}=${localVars[key]}`;
  });

  if (next.length > 0 && next[next.length - 1] !== '') next.push('');
  next.push('# Local smoke overrides generated by scripts/write-local-env.mjs');
  for (const [key, value] of Object.entries(localVars)) {
    if (!used.has(key)) next.push(`${key}=${value}`);
  }

  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, `${next.join('\n')}\n`);
}

upsertEnv(path.join(root, '.env.local'));
upsertEnv(path.join(root, 'apps', 'web', '.env.local'));

console.info('[local-env] wrote .env.local and apps/web/.env.local for local smoke');
